// Операции для вычисления кейсов в switch

public class Operations {
    public static int result;

    public static int plus(int arab1, int arab2) {
        result = arab1 + arab2;
        return result;
    }

    public static int minus(int arab1, int arab2){
        result = arab1 - arab2;
        return result;
    }
    public static int multiply(int arab1, int arab2){
        result = arab1 * arab2;
        return result;
    }
    public static int divide(int arab1, int arab2){
        result = arab1 / arab2;
        return result;
    }
}
